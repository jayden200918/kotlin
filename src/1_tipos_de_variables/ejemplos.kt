package `1_tipos_de_variables`

fun main() {
    // variable inmutable de tipo entero
    val Edad: Int
    Edad = 48
    // variable inmutable de tipo decimal
    val Sueldo: Float
    Sueldo = 1200.55f //siempre lleva una "f" al final del valor
    //variable inmutable de tipo decimal
    val Total: Double
    Total = 70000.24
    //variable inmutable de tipo alfanumerico
    val Titulo: String
    Titulo = "sistema de ventas"
    //Variable mutable, las variables mutables enpiezan con "var"
    var Mes: Int
    Mes = 1
    //Algunas lineas mas de codigo
    Mes = 2
    // variable tipo Char, almacena un unico caracter
    var continua: Char = 's'
    // Variable de tipo Boolean, true o false
    var fin : Boolean = false
    print("jayden")

    println("castro")
    println("saenz")
    }
